def get_MOT_TRAIN(det):
    if det == 'ALL':
        vname = []
        for det in ['SDP', 'DPM', 'FRCNN']:
            vname.extend(get_MOT_TRAIN(det))
        return vname
    else:
        return [ f"{v}-{det}" for v in TRAIN_MOT_VIDEOS ]

def get_MOT_TEST(det):
    if det == 'ALL':
        vname = []
        for det in ['SDP', 'DPM', 'FRCNN']:
            vname.extend(get_MOT_TEST(det))
        return vname
    else:
        return [ f"{v}-{det}" for v in TEST_MOT_VIDEOS ]

def get_MOT_ALL(det):
    if det == 'ALL':
        vname = []
        for det in ['SDP', 'DPM', 'FRCNN']:
            vname.extend(get_MOT_ALL(det))
        return vname
    else:
        return get_MOT_TRAIN(det) + get_MOT_TEST(det)

def create_MOT_vname2split():
    vname2split = {}
    for det in ['SDP', 'DPM', 'FRCNN']:
        for vname in get_MOT_TRAIN(det):
            vname2split[vname] = 'train'
        for vname in get_MOT_TEST(det):
            vname2split[vname] = 'test'
    return vname2split

